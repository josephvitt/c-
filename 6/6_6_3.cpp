/*
指针类型的算术运算
1.指针与整数的加减运算
2.指针++ ，--运算

3.指针p加上或减去n
其意义是指针当前指向位置的前方或者后方的第n个数据的起始位置。
4.指针++ ，--运算
意义是指向下一个或前一个完整数据的起始
5.运算的结果值取决于指针指向的数据类型，总是指向一个完整数据的起始位置
6.当指针指向连续存储的同类型数据时，指针与整数的加减运算和自增自减运算才有意义

*/

/*
指针类型的关系运算
1.指向相同类型数据的指针之间可以进行各种关系运算
2.指向不同数据类型的指针，以及指针与一般整数变量之间的关系运算是无意义的。
3.指针可以和零之间进行等于或者不等于的关系运算。
例： p == 0 或者 p!=0;
*/